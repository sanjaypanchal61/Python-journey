# if statement
age = 18

if age >= 18:
    print("You are eligible to vote")


# if-else statement
age = 16

if age >= 18:
    print("you can vote")
else:
    print("you cannot vote")


# if-elif-else statement
marks = 75

if marks >= 90:
    print("grade A")
elif marks >= 70:
    print("grade B")
elif marks >= 50:
    print("grade C")
else:
    print("fail")