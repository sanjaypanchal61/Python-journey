# example
fruits = ["apple", "banana", "mango"]

print(fruits)


# indexing
fruits = ["apple", "banana", "mango"]

print(fruits[0])
print(fruits[1])


# list slicing
print(fruits[0:2])

# list modify
fruits[1] = "orange"
print(fruits)


# list methods

# append()
fruits.append("grapes")

# insert()
fruits.insert(1,"kiwi")

# remove()
fruits.remove("apple")

# pop()
fruits.pop()

# sort()
num = [6, 3, 10, 2]
num.sort()

# reverse()
num.reverse()

# length
print(len(num))