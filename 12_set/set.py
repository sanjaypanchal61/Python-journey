# set create
numbers = {1, 2, 3, 4}
print(numbers)

# add element
numbers.add(5)
print(numbers)

# remove element
numbers.remove(2)
print(numbers)

# length
print(len(numbers))


# set operations

a = {1, 2, 3}
b = {3, 4, 5}

# union
print(a | b) 

# intersection
print(a & b)

# difference
print(a - b)

